#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*strs;
	size_t	i;

	if (!src && !dst)
		return (dst);
	strs = (char *) src;
	i = 0;
	while (i < n)
	{
		ft_memset(dst + i, strs[i], 1);
		i ++;
	}
	return (dst);
}
