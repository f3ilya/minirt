#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *) s;
	c = (unsigned char) c;
	while (i <= ft_strlen(str))
	{
		if (str[i] == c)
			return (str + i);
		if (str[i] != '\0' && str[i + 1] == '\0' && c == '\0')
			return (str + i + 1);
		i ++;
	}
	return (NULL);
}
