#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;
	t_list	*add;

	temp = *lst;
	while (temp)
	{
		del(temp->content);
		add = temp->next;
		free(temp);
		temp = add;
	}
	*lst = NULL;
}
