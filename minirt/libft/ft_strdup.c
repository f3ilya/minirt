#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	size_t	i;

	cpy = malloc (sizeof (char) * ft_strlen(s1) + 1);
	if (!cpy)
		return (0);
	i = 0;
	while (i < ft_strlen(s1))
	{
		cpy[i] = s1[i];
		i ++;
	}
	cpy[i] = '\0';
	return (cpy);
}
