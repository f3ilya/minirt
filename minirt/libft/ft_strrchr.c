#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *) s;
	c = (unsigned char) c;
	i = ft_strlen(str);
	while (i >= 0)
	{
		if (str[i] == c)
			return (str + i);
		i --;
	}
	return (0);
}
